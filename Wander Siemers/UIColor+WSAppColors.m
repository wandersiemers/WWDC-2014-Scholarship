#import "UIColor+WSAppColors.h"

@implementation UIColor (WSAppColors)

+ (UIColor *)WSBlueColor
{
	return [UIColor colorWithRed:0.138 green:0.710 blue:1.000 alpha:1.000];
}

+ (UIColor *)WSGreenColor
{
	return [UIColor colorWithRed:0.54 green:0.77 blue:0.31 alpha:1.0];
}

@end
